  <h2>О федерации</h2>
  
  <p><strong>МООРС</strong> (Межрегиональная общественная организация развития спидкубинга) — это федерация спидкубинга, которая объединяет любителей скоростной сборки головоломок по всей России.</p>
  
  <h3>Наши цели:</h3>
  <ul>
    <li>Популяризация спидкубинга как интеллектуального вида спорта</li>
    <li>Организация соревнований по скоростной сборке головоломок</li>
    <li>Обучение и развитие спидкуберов всех уровней</li>
    <li>Создание сообщества единомышленников</li>
    <li>Взаимодействие с международными организациями, включая WCA (World Cube Association)</li>
  </ul>
  
  <h3>Наша деятельность:</h3>
  <ul>
    <li>Проведение официальных соревнований по спидкубингу</li>
    <li>Организация мастер-классов и обучающих мероприятий</li>
    <li>Ведение рейтингов спидкуберов России</li>
    <li>Публикация новостей и материалов о спидкубинге</li>
    <li>Поддержка региональных представителей и клубов</li>
  </ul>
  
